def calculate_weighted_average(year_changes, weights):
    """Calculate weighted average of changes."""
    total_weighted = 0
    total_weight = 0

    for age_group, change in year_changes.items():
        if change is not None and age_group in weights:
            weight = weights[age_group]
            total_weighted += change * weight
            total_weight += weight

    if total_weight > 0:
        return total_weighted / total_weight
    return None

